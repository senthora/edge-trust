func TestWriteConfig_CreatesFile(t *testing.T) {
	path := filepath.Join(t.TempDir(), "nginx.conf")
	log := testutil.NewLogger()

	config := Config{content: "hello world"}

	err := config.Write(log, path)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("failed to read file: %v", err)
	}
	assertConfigContent(t, config, string(data))
}

func TestWriteConfig_OverwritesFile(t *testing.T) {
	path := filepath.Join(t.TempDir(), "nginx.conf")
	log := testutil.NewLogger()

	err := os.WriteFile(path, []byte("old content"), 0644)
	if err != nil {
		t.Fatalf("setup failed: %v", err)
	}
	newContent := "new content"
	config := Config{content: newContent}

	err = config.Write(log, path)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("failed to read file: %v", err)
	}
	assertConfigContent(t, config, string(data))
}

func timeNow() (time.Time, string) {
	now := testutil.FixedTimeNowUTC()
	return now, now.UTC().Format(time.RFC3339)
}

func assertConfigContent(t *testing.T, config Config, expected string) {
	t.Helper()

	if config.content != expected {
		t.Fatalf("unexpected content: got %q, want %q", config.content, expected)
	}
}
