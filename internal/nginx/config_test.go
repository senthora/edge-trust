//goland:noinspection GoUnhandledErrorResult
func TestGenerateTrustedProxyConfig_HappyPath(t *testing.T) {
	ips := []string{
		"1.1.1.0/24",
		"2606:4700::/32",
	}
	//goland:noinspection HttpUrlsUsage
	sourceUrl := "http://example.com"
	now, nowFormat := timeNow()

	var b strings.Builder

	fmt.Fprintf(&b, "# Generated by edge-trust at: %s\n", nowFormat)
	fmt.Fprintf(&b, "# Source: %s\n", sourceUrl)
	fmt.Fprintf(&b, "# Total IPs: %d\n\n", len(ips))

	for _, ip := range ips {
		fmt.Fprintf(&b, "set_real_ip_from %s;\n", ip)
	}
	expected := b.String()
	actual := GenerateTrustedProxyConfig(ips, sourceUrl, now)

	assertConfigContent(t, actual, expected)
}

